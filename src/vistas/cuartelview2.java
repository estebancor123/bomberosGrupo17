    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // guardar cuartel
          try{
                        
        int doc=Integer.parseInt(jtCodCuartel.getText());
        String nomb=jtNombreC.getText();
        String direc=jtDireccion.getText();
       int coordX = Integer.parseInt(jtCoordX.getText());
       int coordY = Integer.parseInt(jtCoordY.getText());
       int tel = Integer.parseInt(jtTelefono.getText());
       String correo=jtCorreo.getText();
       boolean est=jrEstado.isSelected();
        
            if (nomb.isEmpty()|| direc.isEmpty() ) {
                JOptionPane.showMessageDialog(this,"llene todos los campos");
                return ;
            }
            if (cua==null) {
                cua=new Cuartel(doc, nomb, direc, coordX,coordY,tel,correo,est);
                cdata.guardarCuartel(cua);
            }else{
                cua.setCodCuartel(doc);
                cua.setDireccion(direc);
                cua.setNombCuartel(nomb);
                cua.setCoorX(coordX);
                cua.setCoorY(coordY);
                cua.setTelefono(tel);
                cua.setCorreo(correo);
                cua.setEstado(est);
                
                cdata.modificarCuartel(cua);
            }
        }catch (NumberFormatException nf){
            JOptionPane.showMessageDialog(this,"datos incorrectos"+nf);
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        // eliminar cuartel
            if (cua!=null) {
            
            cdata.eliminarCuartel(cua.getCodCuartel());
            cua=null;
            limpiarCampos();
        }else{
            JOptionPane.showMessageDialog(this, "debe seleccionar un cuartel");
        }
      
    }//GEN-LAST:event_jbEliminarActionPerformed

    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // buscar cuartel
         try{
            int code=Integer.parseInt(jtCodCuartel.getText());
            
            cua=cdata.buscarCuartel(code);
            
            
                if (cua!=null) {
            
                    jtNombreC.setText(cua.getNombCuartel());
                    jtCoordX.setText(cua.getCoorX()+"");
                    jtCoordY.setText(cua.getCoorY()+"");
                    jtCorreo.setText(cua.getCorreo());
                    jrEstado.setSelected(cua.isEstado());
                    jtTelefono.setText(cua.getTelefono()+"");
                    jtDireccion.setText(cua.getDireccion());

                }else{
                    JOptionPane.showMessageDialog(this, "intent√© otra vez");
                }     
                       
        }catch (NumberFormatException nf){
            JOptionPane.showMessageDialog(this, "error al llenar los campos");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed


